// ✅ MongoDB Basics — Interview Focus
// 1️⃣ What is MongoDB? (Interview Answer)

// MongoDB is a NoSQL, document-oriented database that stores data in flexible JSON-like documents (BSON) instead of tables and rows.
// It is designed for high performance, high availability, and easy scalability.

// ⭐ Key Points (Interview Safe):
// NoSQL (non-relational) database
// Stores data as documents ({ key: value })
// Schema-less → structure can change anytime
// Scales horizontally (sharding support)
// Great for handling large amounts of unstructured data




// 2️⃣ Why MongoDB? (Interview Answer)

// You can say:
// MongoDB is chosen because it gives flexibility, handles large datasets, supports rapid development, and works extremely 
// well with JavaScript-based applications like Node.js.

// ⭐ Benefits:

// Flexible schema → easy to modify fields
// Faster development → JSON-based documents
// High performance for read/write
// Easy to scale horizontally
// Works perfectly with Node.js (MERN)




// 3️⃣ Use Cases of MongoDB (Interview Ready)
// 1. E-Commerce Platforms

// Products have different attributes (size, color, stock)
// Inventory data changes frequently
// ✔ MongoDB handles dynamic data well.
// 2. Social Media Applications
// Users, posts, comments, likes, messages
// ✔ Fast read/write, scalable for millions of users.
// 3. Real-Time Applications

// Chat apps
// Live tracking apps
// ✔ MongoDB handles high-speed unstructured data.
// 4. Content Management Systems (CMS)
// ✔ Different content types, flexible structure.

// 5. IoT & Sensor Data
// ✔ Stores large volume of real-time data.

// 6. Mobile Apps Backend
// ✔ Flexible schema, easy API integration.
// 7. Logging Systems
// ✔ Huge logs, semi-structured data → perfect for MongoDB.




// 4️⃣ When NOT to Use MongoDB (Bonus — Interview Edge)
// Mentioning this shows maturity:
// Banking systems (requires strict ACID and schema)
// Complex multi-table joins
// High transactions with rollback requirements

// 5️⃣ One-Line Interview Definition
// “MongoDB is a NoSQL, document-based database that stores data as JSON-like documents, offering flexible schema and horizontal
//  scalability, widely used in MERN stack applications.”